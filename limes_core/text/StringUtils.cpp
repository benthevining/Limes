#include <cstring>

namespace lemons::cstring
{

bool areSame (const char* string1, const char* string2)
{
	return std::strcmp (string1, string2) == 0;
}

int length (const char* string)
{
	return static_cast<int> (std::strlen (string));
}

}  // namespace lemons::cstring
